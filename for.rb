# Loop through numbers 1 to 5
for i in 1..5
  puts i
end
# Loop through an array of strings
colors = ['red', 'green', 'blue']
for color in colors
  puts color
end
# Loop through numbers 1 to 5 using .each
(1..5).each do |i|
  puts i
end
# Loop through an array of strings using .each
colors = ['red', 'green', 'blue']
colors.each do |color|
  puts color
end
